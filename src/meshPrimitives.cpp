    void Primitive::draw(const unsigned readTex1, const unsigned readTex2, const unsigned readTex3)
    {
        glBindVertexArray(VAO);

        if(readTex1 != 0)
        {
            glActiveTexture(GL_TEXTURE0);
            glBindTexture(GL_TEXTURE_2D, readTex1);
        }

        if(readTex2 != 0){
            glActiveTexture(GL_TEXTURE1);
            glBindTexture(GL_TEXTURE_2D, readTex2);
        }

        if(readTex3 != 0){
            glActiveTexture(GL_TEXTURE2);
            glBindTexture(GL_TEXTURE_2D, readTex3);
        }

        glDrawArrays(GL_TRIANGLES, 0, numVertices);
    }

    void Quad::draw(const unsigned readTex1, const unsigned readTex2, const unsigned readTex3)
    {
        glDisable(GL_DEPTH_TEST);
        Primitive::draw(readTex1, readTex2, readTex3);
    }

    void Quad::setup()
    {
        const float quadVertices[] =
        {
            //positions //texCoordinates
            -1.0f, 1.0f, 0.0f, 1.0f,
            -1.0f, -1.0f, 0.0f, 0.0f,
            1.0f, -1.0f, 1.0f, 0.0f,

            -1.0f, 1.0f, 0.0f, 1.0f,
            1.0f, -1.0f, 1.0f, 0.0f,
            1.0f, 1.0f, 1.0f, 1.0f
        };

        glGenVertexArrays(1, &VAO);
        glGenBuffers(1, &VBO);

        glBindVertexArray(VAO);
        glBindBuffer(GL_ARRAY_BUFFER, VBO);

        glBufferData(GL_ARRAY_BUFFER, sizeof(quadVertices), &quadVertices, GL_STATIC_DRAW);

        glEnableVertexAttribArray(0);
        glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)0);

        glEnableVertexAttribArray(1);
        glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)(2 * sizeof(float)));

        glBindVertexArray(0);
    }

